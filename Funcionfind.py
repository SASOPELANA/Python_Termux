# La función find en Python se utiliza para 
# buscar la posición de una subcadena
# dentro de otra cadena.
# Aquí tienes un ejemplo:

frase = "Hola, soy Sope"
posicion = frase.find("soy")

print(posicion)

# Este código imprimirá el valor 7, ya que la
# subcadena "soy" comienza en la posición
# 7 de la cadena "Hola, soy Sope".
# Si la subcadena no se encuentra, find
# devuelve -1. Es importante mencionar que la 
# búsqueda es sensible a mayúsculas y 
# minúsculas.
