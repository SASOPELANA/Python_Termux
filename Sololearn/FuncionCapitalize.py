# La función capitalize en Python se utiliza para 
# convertir el primer carácter de una cadena (string)
# a mayúscula y el resto de los caracteres a minúscula.
# Aquí tienes un ejemplo:

cadena = "hola mundo"
cadena_capitalizada = cadena.capitalize()

print(cadena_capitalizada)

palabra = "python"
palabra_capitalizada = palabra.capitalize()

print(palabra_capitalizada)


