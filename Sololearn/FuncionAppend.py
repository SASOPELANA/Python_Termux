# En Python, el método append() se utiliza para
# agregar un elemento al final de una lista.
# Aquí hay un ejemplo:

lista = [1, 2, 3]
lista.append(4)
print(lista)  # Esto imprimirá [1, 2, 3, 4]

# Otro ejemplo:

lista = ["manzana", "naranja", "uva"]
lista.append("plátano")
print(lista) 

# Esto imprimirá;
# ['manzana', 'naranja', 'uva', 'plátano']

print("Fin del programa")
