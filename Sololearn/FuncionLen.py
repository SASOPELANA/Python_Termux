# La función `len()` en Python se utiliza
# para obtener la longitud de un objeto, como
# una cadena (string), lista, tupla, o 
# cualquier iterable. Por ejemplo:

cadena = "Hola, mundo!"

longitud_cadena = len(cadena)

print(longitud_cadena)  # Esto imprimirá 13


# También puedes usar `len()` con listas:

lista = [1, 2, 3, 4, 5]

longitud_lista = len(lista)

print(longitud_lista)  # Esto imprimirá 5
print(" ")

movie = "Avatar"
print(len(movie))
