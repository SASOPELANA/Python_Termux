# Las funciones upper() y lower() te permiten
# cambiar rapidamente el caso de una cadena a
# todo mayusculas o minusculas, respectivamente.

minusculas = "CELULARES".lower() # A minusculas

mayusculas = "celulares".upper() # A mayusculas

print("Resultado lower es: ", minusculas)

print("Resultado upper es: ", mayusculas)

print(" ")

